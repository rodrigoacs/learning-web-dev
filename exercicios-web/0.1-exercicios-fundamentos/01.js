function operacoes(valorA, valorB) {
    console.log(`${valorA}+${valorB}= ${valorA + valorB}`);
    console.log(`${valorA}-${valorB}= ${valorA - valorB}`);
    console.log(`${valorA}*${valorB}= ${valorA * valorB}`);
    console.log(`${valorA}/${valorB}= ${valorA / valorB}`);
}

operacoes(10, 5);
operacoes(5, 10);
operacoes(3, 3);