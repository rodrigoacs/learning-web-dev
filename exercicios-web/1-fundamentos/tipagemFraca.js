var qualquer;

//variaveis do tipo string
qualquer = 'palavra';
console.log(qualquer, typeof qualquer);
qualquer = 'p';
console.log(qualquer, typeof qualquer);

//variaveis do tipo number
qualquer = 3.14;
console.log(qualquer, typeof qualquer);
qualquer = 3;
console.log(qualquer, typeof qualquer);

//variaveis do tipo boolean
qualquer = false;
console.log(qualquer, typeof qualquer);
qualquer = true;
console.log(qualquer, typeof qualquer);
